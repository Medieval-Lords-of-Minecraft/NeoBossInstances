package me.neoblade298.neobossinstances;

public class SpawnerSet {
	String internal;
	int max;
	public SpawnerSet(String internal, int max) {
		this.internal = internal;
		this.max = max;
	}
	public String getInternal() {
		return internal;
	}
	public int getMax() {
		return max;
	}
}
